package HomeWork2.add.NumberOne;

public class Primitives {
    private static final byte a = 1;
    private static final short b = 5;
    private static final int c = 88;
    private static final long d = 123456789L;
    private static final float e = 1.35F;
    private static final double f = 3.1234567;
    private static final char g = 'x';
    private static final boolean h = false;

    public static void main(String[] args) {
        System.out.println("byte : " + a);
        System.out.println("short : " + b);
        System.out.println("int " + c);
        System.out.println("long " + d);
        System.out.println("float " + e);
        System.out.println("double " + f);
        System.out.println("char " + g);
        System.out.println("boolean " + h);
    }
}